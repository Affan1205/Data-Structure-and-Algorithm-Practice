//Read N and N integers, then print each element multiplied by 2 on one line. Aim: compute on the fly.

public class PracticeQues1DArray12 {
    public static void main(String[] args) {
        int[] arr = { 23, 32, 34, 54, 2 };
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] * 2 + " ");
        }
    }
}
